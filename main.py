@app.route('/admin/scrape', methods=['POST'])
@permission_required()
def scrape():
	from scraper import scraper
	scraper.event_names()
	scraper.event_details()
	scraper.tpids()
	scraper.team_details()
	scraper.match(CURRENT_EVENT)
	return {'200 Ok': 'Scrape successful'}

